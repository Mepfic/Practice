package listOfUsers;

public class User {
    public String firstName;
    public String lastName;
    public int age;
    public String gender;
    public boolean ifStudent;
    public float rating;

    public String getFirstName() {
        return firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public int getAge() {
        return age;
    }

    public String getGender() {
        return gender;
    }

    public boolean getIfStudent() {
        return ifStudent;
    }

    public float getRating() {
        return rating;
    }

}
